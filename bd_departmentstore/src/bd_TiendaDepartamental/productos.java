    public void actualizarDatos(){
    
    try {
        
        String SQL="update productos set  IDproducto=?,Tipo=?,Marca=?,Talla=?,Color=?,Presio=?,Cantidad=? where IDproducto=?";
        
        int fla=tablaclientes.getSelectedRow();
        String dao=(String)tablaclientes.getValueAt(fla,0);
        
        
        
        PreparedStatement pst=con.prepareStatement(SQL);
        
        pst.setString(1, txtproducto.getText());
        pst.setString(2, txttipo.getText());
        pst.setString(3, txtmarca.getText());
        pst.setString(4, txttalla.getText());
        pst.setString(5, txtcolor.getText());
        pst.setString(6, txtpresio.getText());
        pst.setString(7, txtcantidad.getText());
        
        pst.setString(8, dao);
        pst.execute();
        
        JOptionPane.showMessageDialog(null,"Regiatro Editado Exitoso");
        
        
    } catch (Exception e){
        JOptionPane.showMessageDialog(null,"Error de Edicion" + e.getMessage());
    }
}
    
