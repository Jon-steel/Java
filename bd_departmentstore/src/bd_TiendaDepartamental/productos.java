        public void filtrardatos(String valor){
    
        String[] titulo={"IDproducto","Tipo","Marca","Talla","Color","Presio","Cantidad"};
        String[] registros=new String[8];
        
        DefaultTableModel modelo=new DefaultTableModel(null,titulo);
        String SQL="select * from productos where Tipo like '%"+valor+"%'";
        
         try {
        
             Statement st=con.createStatement();
             ResultSet rs=st.executeQuery(SQL);
             
             while (rs.next()){
                 
                 registros[0]=rs.getString("IDproducto");
                  registros[1]=rs.getString("Tipo");
                   registros[2]=rs.getString("Marca");
                    registros[3]=rs.getString("Talla");
                     registros[4]=rs.getString("Color");
                      registros[5]=rs.getString("Presio");
                       registros[6]=rs.getString("Cantidad");
                        
             modelo.addRow(registros);
             }
        tablaclientes.setModel(modelo);
        
    } catch (Exception e){
        JOptionPane.showMessageDialog(null,"Error al mostrar datos" + e.getMessage());
    }
}
    
    
